        /*
         La propiedad Interval indica al temporizador cuánto tiempo debe 
         esperar entre los tics cuando desencadena el evento Tick. El 
         programa llama al método Start() para iniciar el temporizador 
         después de que el jugador elige la segunda etiqueta.
         */
        private void timer1_Tick(object sender, EventArgs e)
        {
            // Stop the timer
            timer1.Stop();

            // Hide both icons
            firstClicked.ForeColor = firstClicked.BackColor;
            secondClicked.ForeColor = secondClicked.BackColor;

            // Reset firstClicked and secondClicked 
            // so the next time a label is
            // clicked, the program knows it's the first click
            firstClicked = null;
            secondClicked = null;
        }

        private void CheckForWinner()
        {
            //Comprueba el color del icono de cada etiqueta para ver si coincide
            //con el fondo. Si los colores coinciden, el icono sigue siendo
            //invisible y el jugador no ha hallado las parejas de los iconos
            //restantes.
            foreach (Control control in tableLayoutPanel1.Controls) 
            {
                Label iconLabel = control as Label;

                if (iconLabel != null)
                {
                    if(iconLabel.ForeColor == iconLabel.BackColor)
                        return;
                }      
            }

            //Si el bucle pasa por todas las etiquetas sin ejecutar la
            //instrucción return, indica que se han logrado hallar todas
            //las parejas de iconos del formulario
            MessageBox.Show("¡Has ganado!","Enhorabuena");
            Close();
        }
