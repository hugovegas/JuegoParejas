        // Para generar los iconos aleatoriamente
        Random random = new Random();

        // Listado de letras con icono bonito en la fuente Webdings.
        // Cada icono debe aparecer 2 veces en la lista para formar parejas
        List<string> icons = new List<string>()
        {
            "!", "!", "N", "N", ",", ",", "k", "k",
            "b", "b", "v", "v", "w", "w", "z", "z"
        };

        // Primera etiqueta clicada
        Label? firstClicked = null;

        // Segunda etiqueta clicada
        Label? secondClicked = null;

        private void AssingIconsToSquares()
        {
            // Añadimos un icono a cada una de las labels del tablero
            foreach (Control control in tableLayoutPanel1.Controls)
            {
                Label iconLabel = control as Label;
                if (iconLabel != null)
                {
                    int randomNumber = random.Next(icons.Count);
                    iconLabel.Text = icons[randomNumber];
                    iconLabel.ForeColor = iconLabel.BackColor;
                    icons.RemoveAt(randomNumber);
                }
            }
        }

        public TableroParejas()
        {
            InitializeComponent();

            AssingIconsToSquares();
        }

        private void label1_Click(object sender, EventArgs e)
        {
            // Comprueba si el temporizador se ha iniciado comprobando el valor
            // de la propiedad Enabled. Si el jugador elige el primer y segundo
            // control Label y se inicia el temporizador, no sucederá nada al
            // elegir un tercer control.
            if (timer1.Enabled == true)
                return;

            Label clickedLabel = sender as Label;

            if (clickedLabel != null)
            {
                // If the clicked label is black, the player clicked
                // an icon that's already been revealed --
                // ignore the click
                if (clickedLabel.ForeColor == Color.Black)
                    return;

                if (firstClicked == null)
                {
                    firstClicked = clickedLabel;
                    clickedLabel.ForeColor = Color.Black;

                    return;
                }

                // Establece la variable de referencia secondClicked para
                // realizar un seguimiento del segundo control Label. Luego,
                // establece el color del icono de esa etiqueta en negro para
                // que sea visible. 
                secondClicked = clickedLabel;
                secondClicked.ForeColor = Color.Black;

                CheckForWinner();

                // If the player clicked two matching icons, keep them 
                // black and reset firstClicked and secondClicked 
                // so the player can click another icon
                if (firstClicked.Text == secondClicked.Text)
                {
                    firstClicked = null;
                    secondClicked = null;
                    return;
                }

                // A continuación, inicia el temporizador en modo de un disparo
                // de forma que espere 750 milisegundos antes de desencadenar un
                // tic único. El controlador de eventos Tic del temporizador
                // oculta los dos iconos y restablece las variables de
                // referencia firstClicked y secondClicked.
                timer1.Start();
            }
        }

        private void label2_Click(object sender, EventArgs e)
        {
            // The timer is only on after two non-matching 
            // icons have been shown to the player, 
            // so ignore any clicks if the timer is running
            if (timer1.Enabled == true)
                return;

            Label clickedLabel = sender as Label;

            if (clickedLabel != null)
            {
                // If the clicked label is black, the player clicked
                // an icon that's already been revealed --
